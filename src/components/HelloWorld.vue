  </div>
</template>

<script>
export default {
  name: 'HelloWorld',
  data () {
    return {
      msg: 'Welcome to Your Vue.js App'
    }
  },
  methods: {
    updateText() {
      this.$emit('updateText', this.msg)
    }
  }
}
</script>

